void PrintImage(int[,] image)
{
    for(int row = 0; row < image.GetLength(0); row++)
    {
        for(int col = 0; col < image.GetLength(1); col++)
        {
            if(image[row, col] == 0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if(pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col  + 1);
    }
}

